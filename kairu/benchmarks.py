"""Per-criterion percentile benchmarks from a synthetic reference corpus.

What this module is
-------------------
A deterministic, in-memory reference distribution for every criterion in
:mod:`kairu.evaluation.CRITERIA`. The distribution is generated *once at
import time* by running each criterion's scorer over a fixed corpus of
1000 synthetic (prompt, response) pairs derived from a seeded LCG, so the
percentile cuts are stable across processes, machines, and Python versions
(no NumPy random state, no OS RNG, no time-dependent seeds).

The corpus is **not** a benchmark of any real model — it is a *reference
distribution* that gives every demo and every UI a sensible "where does
this score sit?" comparison without requiring users to bring their own
calibration set.

Public surface
--------------
- ``BENCHMARK_CORPUS_SIZE`` — 1000, fixed.
- ``BENCHMARKS`` — ``Dict[criterion_name, BenchmarkStats]``.
- :class:`BenchmarkStats` — dataclass with ``p25/p50/p75/p90/p99/mean/stdev``
  plus a 20-point histogram of the distribution (for violin / sparkline
  rendering on the wire) and the criterion name.
- :func:`percentile_rank(criterion, value)` — returns ``[0, 1]``; the
  fraction of the reference corpus whose score is *less than or equal to*
  ``value``. Cheap O(log n) bisect.
"""
from __future__ import annotations

import hashlib
import math
import statistics
from dataclasses import dataclass, field
from typing import Dict, List, Mapping, Sequence, Tuple

from kairu.evaluation import CRITERIA, ScorerResult

BENCHMARK_CORPUS_SIZE: int = 1000
HISTOGRAM_BUCKETS: int = 20


# ─────────────────────────────────────────────────────────────────────────
# Deterministic synthetic corpus
#
# We need 1000 (prompt, response) pairs with realistic enough diversity
# that the criterion scorers produce a spread across [0, 1]. Strategy:
#
#   * Choose a fixed bank of ~30 prompt seeds covering common task shapes
#     (factual Q&A, code, creative, summarization, instruction follow).
#   * Choose a bank of ~30 response styles ("verbose with citations",
#     "single-line refusal", "PII-leaky", "list-formatted", etc.).
#   * For pair k in [0, 1000), prompt = bank[k % 30], style = bank[k * 7 % 30],
#     and the *content* of the response is generated by a tiny seeded LCG
#     drawing tokens from a fixed vocabulary, with the chosen style
#     controlling length / repetition / pii density.
#
# This is enough to spread every scorer in `CRITERIA` across most of [0, 1]
# without inheriting any actual model output. The corpus is generated once
# at module import; the percentile cuts are stable forever after.
# ─────────────────────────────────────────────────────────────────────────


