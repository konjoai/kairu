def _lcg(seed: int) -> int:
    """Knuth multiplicative LCG step — deterministic, no NumPy."""
    return (seed * 2654435761) & 0xFFFFFFFF


def _build_pair(k: int) -> Tuple[str, str]:
    """Construct the k-th (prompt, response) reference pair."""
    prompt = _PROMPT_BANK[k % len(_PROMPT_BANK)]
    style_name, style_template = _RESPONSE_STYLES[(k * 7 + 3) % len(_RESPONSE_STYLES)]
    seed = _lcg(k * 31 + 7)
    # Inject variation: extend the template with seed-derived suffixes so
    # different k's hit different length / repetition profiles for the same style.
    suffix_len = 1 + (seed % 12)
    suffix = " word" * suffix_len
    repeat = 1 + ((seed >> 4) % 3)
    body = (style_template + suffix) * repeat
    return prompt, body


def _generate_corpus() -> List[Tuple[str, str]]:
    return [_build_pair(k) for k in range(BENCHMARK_CORPUS_SIZE)]


# ─────────────────────────────────────────────────────────────────────────
# Score the corpus once per criterion, freeze the distribution
# ─────────────────────────────────────────────────────────────────────────


@dataclass(frozen=True)
class BenchmarkStats:
    criterion: str
    n: int
    mean: float
    stdev: float
    p25: float
    p50: float
    p75: float
    p90: float
    p99: float
    histogram: Tuple[int, ...]      # 20-bucket count over [0, 1]
    samples_hash: str               # Sanity-check determinism across builds

    def percentile_rank(self, value: float) -> float:
        """O(log n) bisect over the sorted samples — but we don't carry the
        sorted samples in the frozen stats. Use the 20-bucket histogram as a
        coarse approximation; the violin viz only needs ~5% resolution."""
        if value <= 0.0:
            return 0.0
        if value >= 1.0:
            return 1.0
        bucket = min(HISTOGRAM_BUCKETS - 1, int(value * HISTOGRAM_BUCKETS))
        # Cumulative count up to (and including) this bucket.
        cumulative = sum(self.histogram[: bucket + 1])
        # Linear interpolation inside the bucket: fraction of bucket we've passed.
        bucket_lo = bucket / HISTOGRAM_BUCKETS
        bucket_hi = (bucket + 1) / HISTOGRAM_BUCKETS
        frac = (value - bucket_lo) / (bucket_hi - bucket_lo) if bucket_hi > bucket_lo else 0.0
        within_bucket = self.histogram[bucket] * frac
        approx_below = cumulative - self.histogram[bucket] + within_bucket
        return max(0.0, min(1.0, approx_below / self.n))

    def to_dict(self) -> Dict[str, object]:
        return {
            "criterion": self.criterion,
            "n": self.n,
            "mean": round(self.mean, 6),
            "stdev": round(self.stdev, 6),
            "p25": round(self.p25, 6),
            "p50": round(self.p50, 6),
            "p75": round(self.p75, 6),
            "p90": round(self.p90, 6),
            "p99": round(self.p99, 6),
            "histogram": list(self.histogram),
            "buckets": HISTOGRAM_BUCKETS,
            "samples_hash": self.samples_hash,
        }


def _quantile(sorted_values: Sequence[float], q: float) -> float:
    """Linear interpolation between adjacent samples; matches numpy's
    ``np.quantile(..., method="linear")``. ``q`` in ``[0, 1]``."""
    if not sorted_values:
        return 0.0
    if len(sorted_values) == 1:
        return sorted_values[0]
    pos = q * (len(sorted_values) - 1)
    lo = math.floor(pos)
    hi = math.ceil(pos)
    if lo == hi:
        return sorted_values[lo]
    frac = pos - lo
    return sorted_values[lo] + (sorted_values[hi] - sorted_values[lo]) * frac


def _build_stats(name: str, samples: List[float]) -> BenchmarkStats:
    sorted_samples = sorted(samples)
    histogram = [0] * HISTOGRAM_BUCKETS
    for v in samples:
        idx = min(HISTOGRAM_BUCKETS - 1, max(0, int(v * HISTOGRAM_BUCKETS)))
        histogram[idx] += 1
    # Stable hash of the raw samples — invariant across processes given the
    # same corpus + scorer logic. Used in tests + smoke-checks.
    digest = hashlib.sha256(
        ",".join(f"{v:.6f}" for v in sorted_samples).encode("utf-8")
    ).hexdigest()[:12]
    return BenchmarkStats(
        criterion=name,
        n=len(samples),
        mean=statistics.fmean(samples),
        stdev=statistics.pstdev(samples) if len(samples) > 1 else 0.0,
        p25=_quantile(sorted_samples, 0.25),
        p50=_quantile(sorted_samples, 0.50),
        p75=_quantile(sorted_samples, 0.75),
        p90=_quantile(sorted_samples, 0.90),
        p99=_quantile(sorted_samples, 0.99),
        histogram=tuple(histogram),
        samples_hash=digest,
    )


def _build_benchmarks() -> Dict[str, BenchmarkStats]:
    corpus = _generate_corpus()
    out: Dict[str, BenchmarkStats] = {}
    for criterion_name, (scorer, _desc) in CRITERIA.items():
        samples = []
        for prompt, response in corpus:
            try:
                result: ScorerResult = scorer(prompt, response)
                samples.append(float(result.score))
            except Exception:  # noqa: BLE001 — defensive; any single failure should not break the table
                samples.append(0.0)
        out[criterion_name] = _build_stats(criterion_name, samples)
    return out


# Build once at import time — every consumer sees the same frozen table.
BENCHMARKS: Mapping[str, BenchmarkStats] = _build_benchmarks()


def percentile_rank(criterion: str, value: float) -> float:
    """Return the fraction of the reference corpus with score <= ``value``."""
    if criterion not in BENCHMARKS:
        raise KeyError(f"unknown criterion '{criterion}'")
    return BENCHMARKS[criterion].percentile_rank(value)


